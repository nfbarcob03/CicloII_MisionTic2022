    private void libroTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_libroTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) libroTable.getModel();
        int selectRowIndex = libroTable.getSelectedRow();
        idLibro = Integer.parseInt(model.getValueAt(selectRowIndex, 0).toString());
        tituloText.setText(model.getValueAt(selectRowIndex, 1)!= null? model.getValueAt(selectRowIndex, 1).toString():"");
        editorialText.setText(model.getValueAt(selectRowIndex, 2)!= null? model.getValueAt(selectRowIndex, 2).toString():"");
        areaText.setText(model.getValueAt(selectRowIndex, 3)!= null? model.getValueAt(selectRowIndex, 3).toString():"");
        
    }//GEN-LAST:event_libroTableMouseClicked

    private void axtualizarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_axtualizarBotonActionPerformed
        // TODO add your handling code here:
        Libro libro = recogerCampos();
        try {
            libroControlador.actualizarLibro(libro);
            idLibro =0;
            tituloText.setText("");
            editorialText.setText("");
            areaText.setText("");
            mostrarTabla();
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "no se puede actualizar el libro");
        }
        
    }//GEN-LAST:event_axtualizarBotonActionPerformed

