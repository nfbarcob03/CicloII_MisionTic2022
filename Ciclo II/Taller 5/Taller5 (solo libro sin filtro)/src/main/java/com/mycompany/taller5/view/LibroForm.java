    public void mostrarTabla(){
        String[] campos = {"IdLibro", "Titulo", "Editorial", "Area"};
        //definir un modelo
        DefaultTableModel modeloDatos = new DefaultTableModel(campos, 0);
        //asignar el modelo a la tabla
        libroTable.setModel(modeloDatos);
        
        ArrayList<Libro> librosBD = libroControlador.consultarLibros();

        for (Libro libro:librosBD){
            Object obejetoDatos [] = {libro.getIdLibro(),libro.getTitulo(), libro.getEditorial(), libro.getArea()};
            modeloDatos.addRow(obejetoDatos);
        }
  
    }
    
    public Libro recogerCampos (){
        Libro libAux =  new Libro(idLibro,tituloText.getText(), editorialText.getText(), areaText.getText());
        
        return libAux;
    }
