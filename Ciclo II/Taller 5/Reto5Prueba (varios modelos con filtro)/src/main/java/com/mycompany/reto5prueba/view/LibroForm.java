        private void mostrarTabla() {

        String[] campos = {"IdLibro", "Titulo", "Editorial", "Area"};
        
        //definir un modelo
        DefaultTableModel modeloDatos = new DefaultTableModel(campos, 0);
        //asignar el modelo a la tabla
        libroTable.setModel(modeloDatos);
        
        String tituloEntrada = this.titulo.getText();
        String editorialEntrada= this.editorial.getText();
        String areaEntrada = this.area.getText();
        
        Libro libroEntrada = recogerCampos();
        
        
        ArrayList<Libro> libros = libroControlador.consultarJuguetes(libroEntrada);        //consultar y agregar los datos a la tabla

        for (Libro libro: libros) {
            Integer idLibro = libro.getIdTitulo();
            String titulo = libro.getTitulo();
            String editorial = libro.getEditorial();
            String area = libro.getArea();

            Object objetoDatos[] = {idLibro, titulo, editorial, area};
            modeloDatos.addRow(objetoDatos);
        } 

    }
    
