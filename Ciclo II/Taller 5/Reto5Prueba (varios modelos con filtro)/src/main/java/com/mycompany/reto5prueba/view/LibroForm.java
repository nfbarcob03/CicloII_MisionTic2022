    public Libro recogerCampos(){
        int idLibro = this.idLibro;
        String tituloEntrada = this.titulo.getText()!= null? this.titulo.getText():"";
        String editorialEntrada = this.editorial.getText()!= null? this.editorial.getText():"";
        String area = this.area.getText()!= null? this.area.getText():"";
        
        Libro nuevoLibro = new Libro(idLibro,tituloEntrada,editorialEntrada, area);
        
        return nuevoLibro;
    }
