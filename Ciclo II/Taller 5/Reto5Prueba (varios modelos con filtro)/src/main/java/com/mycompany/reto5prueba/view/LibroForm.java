    private void libroTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_libroTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) libroTable.getModel();
        int selectRowIndex = libroTable.getSelectedRow();
        idLibro = Integer.parseInt(model.getValueAt(selectRowIndex, 0).toString());
        titulo.setText(model.getValueAt(selectRowIndex, 1)!= null? model.getValueAt(selectRowIndex, 1).toString(): "");
        editorial.setText(model.getValueAt(selectRowIndex, 2)!= null? model.getValueAt(selectRowIndex, 2).toString(): "");
        area.setText(model.getValueAt(selectRowIndex, 3)!= null? model.getValueAt(selectRowIndex, 3).toString(): "");
        
    }//GEN-LAST:event_libroTableMouseClicked

